    /**
     * 转换时间戳为剩余时分秒
     *
     * @param differTime 要转换的时间戳,单位为s
     * @return the string
     * @User: xiemiao
     * @Time: 2017 -08-29
     * @Date: 10 :41:27
     */
    public static String parseRemainderHMS(long differTime) {
        int H = (int) (differTime / 3600);//获取小时
        differTime = differTime % 3600;//取余
        int M = (int) (differTime / 60);//获取分钟
        differTime = differTime % 60;//取余
        int S = (int) differTime;//获取秒
        return H + "小时" + M + "分钟" + S + "秒";
    }

